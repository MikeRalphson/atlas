package org.atlasapi.remotesite.youtube;


public class YouTubeException extends Exception {

    private static final long serialVersionUID = 6704092963586185068L;

    public YouTubeException() {
    }

    public YouTubeException(String arg0) {
        super(arg0);
    }

    public YouTubeException(Throwable arg0) {
        super(arg0);
    }

    public YouTubeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

}
