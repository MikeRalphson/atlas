package org.atlasapi.remotesite.bbc.ion.model;

import java.net.URL;

public class IonGenerator {

    private String version;
    private String type;
    private String title;
    private URL uri;

    public String getVersion() {
        return version;
    }

    public String getType() {
        return type;
    }

    public String getTitle() {
        return title;
    }

    public URL getUri() {
        return uri;
    }

}
